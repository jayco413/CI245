/**
 * The HelloWorld class demonstrates a basic Java application structure.
 * This class contains a single method, the main method, which is the entry
 * point of any Java application.
 * 
 * @author Jason A. Covey
 */
public class HelloWorld {

    /**
     * The main method is the entry point of a Java application.
     * @param args Command line arguments passed to the program. In this
     *             simple example, args are not used.
     */
    public static void main(String[] args) {
        // System.out.println is used for printing text to the console.
        // It is similar to 'cout' in C++, but it is part of the Java
        // System class and not a global object like 'cout'.
        // 'System' is a predefined class that provides access to the system,
        // 'out' is the output stream connected to the console, and
        // 'println' is a method to print a line of text to the console.
        System.out.println("Hello World");
    }
}
